package com.example.assigment_android_nangcao.model;

public class KhoaHoc {
    public int maKhoaHoc;
    public String tenKhoaHoc;
    public String giaKhoaHoc;
    public String giangVienKhoaHoc;
    public String noiDungKhoaHoc;
    public String ghiChuKhoaHoc;

    public int getMaKhoaHoc() {
        return maKhoaHoc;
    }

    public void setMaKhoaHoc(int maKhoaHoc) {
        this.maKhoaHoc = maKhoaHoc;
    }

    public String getTenKhoaHoc() {
        return tenKhoaHoc;
    }

    public void setTenKhoaHoc(String tenKhoaHoc) {
        this.tenKhoaHoc = tenKhoaHoc;
    }

    public String getGiaKhoaHoc() {
        return giaKhoaHoc;
    }

    public void setGiaKhoaHoc(String giaKhoaHoc) {
        this.giaKhoaHoc = giaKhoaHoc;
    }

    public String getGiangVienKhoaHoc() {
        return giangVienKhoaHoc;
    }

    public void setGiangVienKhoaHoc(String giangVienKhoaHoc) {
        this.giangVienKhoaHoc = giangVienKhoaHoc;
    }

    public String getNoiDungKhoaHoc() {
        return noiDungKhoaHoc;
    }

    public void setNoiDungKhoaHoc(String noiDungKhoaHoc) {
        this.noiDungKhoaHoc = noiDungKhoaHoc;
    }

    public String getGhiChuKhoaHoc() {
        return ghiChuKhoaHoc;
    }

    public void setGhiChuKhoaHoc(String ghiChuKhoaHoc) {
        this.ghiChuKhoaHoc = ghiChuKhoaHoc;
    }
}
